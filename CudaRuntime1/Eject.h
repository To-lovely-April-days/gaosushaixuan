#pragma once
// ============================================================================
// 文件名：Eject.h
// 作用  ：阀控通信模块（串口 + 按帧动态延迟补偿）
//
// v2 重大改动（相对旧版）：
//   1. 启动时一次性生成阀号映射表 g_valveMap[192]（含倒序映射，运行时纯查表）
//   2. 处理粒度：批 → 帧（每帧独立判断 + 独立发命令）
//   3. 新增 g_valveThreshold 参数：阀对应像素中命中数 ≥ 此值才吹
//   4. 删除：5x5 过滤、阀位膨胀、批中点延迟模式
//   5. 优化：每帧多条命令打包一次 WriteFile 发送
// ============================================================================
#include <string>
#include <atomic>

// ============================================================================
// 常量
// ============================================================================
extern const int VALVE_COUNT;   // 阀总数（192）

// ============================================================================
// ★ 阀控通信方式开关
//   VALVE_COMM_SERIAL = 0  旧方式：串口 (COM 口) WriteFile, 每段 7 字节帧
//   VALVE_COMM_UDP    = 1  新方式：千兆网 UDP 发往接口板 (默认)
//                          目标 192.168.1.1:10100, 每段 20 字节帧
//                          (14 字节固定包头 + 阀起2 + 阀止2 + 延时1 + 时长1)
//   默认 = VALVE_COMM_UDP。需要切回串口时在 Start_send 之前调
//   SetValveCommMode(VALVE_COMM_SERIAL)。
// ============================================================================
enum ValveCommMode { VALVE_COMM_SERIAL = 0, VALVE_COMM_UDP = 1 };
extern std::atomic<int> g_valveCommMode;   // 默认 VALVE_COMM_UDP

// 切换阀控通信方式（应在 Start_send 之前调用）
void SetValveCommMode(int mode);
// 配置 UDP 阀控目标接口板地址（默认 192.168.1.1:10100）
void SetValveUdpTarget(const std::string& ip, unsigned short port);

// ============================================================================
// 心跳字节（保留，UDP 接收线程仍用）
// ============================================================================
extern char firstByte;

// ============================================================================
// 阀号映射表（启动时一次性生成，含倒序映射）
//   g_valveMap[fa] 对应物理阀号 (fa+1) 的像素区间 [x1, x2]
// ============================================================================
struct ValveRange {
    int x1;   // 起始像素（含）
    int x2;   // 结束像素（含）
};
extern ValveRange g_valveMap[];        // 大小 = VALVE_COUNT
extern bool       g_valveMapReady;     // 是否已初始化

// ============================================================================
// 关键参数
// ============================================================================
//
// 阀触发阈值：阀对应的像素中，命中目标分类的数量 ≥ 此值才吹
//   1 = 任意 1 个像素命中就吹（最敏感，等同旧逻辑）
//   2 = 至少 2 个像素命中才吹（中等抗噪）
//   3 = 全部 3 个像素都命中才吹（最严格）
extern int g_valveThreshold;

extern int g_valveThresholdRatio;

extern int g_centerValveInflate;
// 帧激活阈值：本帧目标像素总数必须 ≥ 此值，本帧才会发吹气命令
//   0  = 不启用帧激活（任何帧都吹）
//   N  = 本帧总命中像素数 < N 时，整帧抑制（屏蔽斑点误吹）
//   推荐起步: 5
extern int g_frameActivateThreshold;
// 总飞行时间（ms）：物料从相机视野中心 → 喷阀位置的预估时间
//   按帧补偿后，elapsed 范围会扩大，需要比批中点模式更大的值
//   推荐 45ms（按帧模式下的典型值）
extern unsigned short g_totalDelay;

// 当前正在处理的帧的拍摄时刻（分发线程在每帧处理前更新）
//   OpenAirCompensatedFrame 读取此值算 actualDelay
extern std::atomic<unsigned long long> g_currentFrameTick;

// ============================================================================
// 主入口（按帧版本）
// ============================================================================
//
// ProcessOneFrame: 处理一帧的 640 像素分类结果
//   frameTags    : 这一帧的 g_Samples 个 tag（每像素一个 unsigned char 分类 ID）
//   frameTickMs  : 这一帧的拍摄时刻（QPC 毫秒）
//
// 内部流程：
//   1. 查 g_valveMap，对每个阀统计命中数 → 按 valveThreshold 判定
//   2. 合并连续阀号成区间
//   3. 这一帧的所有命令打包成一个连续 buffer，一次 WriteFile 发出
void ProcessOneFrame(const char* frameTags, unsigned long long frameTickMs);

// ============================================================================
// 串口控制
// ============================================================================
bool Start_send();
void Stop_send();

void SetSerialPort(const std::string& comPort, int baudRate);
void SetEjectParams(unsigned short durationMs, unsigned short delayMs);
void SetPixelOffset(int frontOffset, int backOffset);

// ============================================================================
// 阀号映射表构造（Start_send 末尾会自动调一次）
//   也可以手动调用以便重建（比如 g_Samples 变了之后）
// ============================================================================
void BuildValveMap();

// ============================================================================
// ★ 参数快照：把当前全部运行参数写入 perf.log
//   tag 用于标注时机（如 "run start"）。
//   必须在 Eject.cpp 中实现，因为它要读取本模块内的 static 变量（g_duration）。
// ============================================================================
void LogAllParams(const char* tag);

// ============================================================================
// UDP 参数接收线程（操作屏可下发 sel_type）
//   注意：percent[] 和 fa_ctl 在 v2 已不使用，但接收逻辑保留兼容
// ============================================================================
void UDP_receive_thread();

// ============================================================================
// 工具函数：获取当前 QPC 毫秒（main.cpp 也要用，暴露出来）
// ============================================================================
unsigned long long NowMs();